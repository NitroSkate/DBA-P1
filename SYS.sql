CREATE ROLE CATASSIST;

GRANT CREATE SESSION TO CATASSIST;
GRANT SELECT ON USERT TO CATASSIST;
GRANT SELECT ON USERP TO CATASSIST;
GRANT CREATE TABLE TO CATASSIST;

GRANT CATASSIST TO SYSASSIST;

SELECT * FROM DBA_USERS WHERE USERNAME = 'SYSASSIST';
SELECT * FROM USER_ROLE_PRIVS ;
SELECT * FROM USER_TAB_PRIVS WHERE GRANTEE = 'SYSASSIST';
